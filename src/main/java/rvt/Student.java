package rvt;

public class Student {
    public String name;
    public String surname;
    public String email;
    public String group;
    public String word;


    public Student(String name, String surname, String email, String group, String word) {
        this.name = name;
        this.surname = surname;
        this.email = email;
        this.group = group;
        this.word = word;
    }
}
